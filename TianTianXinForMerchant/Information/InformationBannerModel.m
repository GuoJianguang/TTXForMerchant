#import "InformationBannerModel.h"

@implementation InformationBannerModel

+(id)modelWithDic:(NSDictionary *)dic
{
    InformationBannerModel *model = [[InformationBannerModel alloc]init];
    model.cover = NullToSpace(dic[@"cover"]);
    model.createTime = NullToSpace(dic[@"createTime"]);
    model.detailUrl = NullToSpace(dic[@"detailUrl"]);
    model.informationId = NullToSpace(dic[@"id"]);
    model.mark = NullToSpace(dic[@"mark"]);
    model.title = NullToSpace(dic[@"title"]);
    return model;
}

@end
